-- CREATE DATABASE pokemon_db;

USE pokemon_db;


DROP TABLE IF EXISTS PokeRefs; -- ALL REFERENCE TABLES WITH FOREIGN KEYS NEED TO BE FIRST
DROP TABLE IF EXISTS Pokemon;
DROP TABLE IF EXISTS PokeTypes;
DROP TABLE IF EXISTS SubPokeTypes;
DROP TABLE IF EXISTS Abilities;
DROP TABLE IF EXISTS SubAbilities;
DROP TABLE IF EXISTS HiddenAbilities;
DROP TABLE IF EXISTS Stats;
DROP TABLE IF EXISTS Regions;


-- ====================================== POKEMON AKA CREATE TABLES ====================================== 
CREATE TABLE Pokemon (
	pokemon_id INT PRIMARY KEY NOT NULL,
    pokedex_num DOUBLE,
    pokemon_name TEXT,
    height_m DOUBLE,
    weight_kg DOUBLE,
    special_type TEXT,
    evolves BOOLEAN
);

-- -------------------------------------- POKETYPES -------------------------------------- 
CREATE TABLE PokeTypes (
	type_id INT PRIMARY KEY,
    type_name TEXT
);

CREATE TABLE SubPokeTypes (
	sub_type_id INT PRIMARY KEY,
    sub_type_name TEXT
);

-- -------------------------------------- REGIONS -------------------------------------- 
CREATE TABLE Regions (
	region_id INT PRIMARY KEY,
    region_name TEXT
);

-- ---------------------------------------  ABILITIES -------------------------------------- 
CREATE TABLE Abilities (
	ability_id INT AUTO_INCREMENT PRIMARY KEY NOT NULL,
	ability TEXT,
    ability_effect TEXT
);

CREATE TABLE SubAbilities (
	sub_ability_id INT AUTO_INCREMENT PRIMARY KEY,
	sub_ability TEXT,
	sub_ability_effect TEXT
);


CREATE TABLE HiddenAbilities (
	hidden_ability_id INT AUTO_INCREMENT PRIMARY KEY,
	hidden_ability TEXT,
    hidden_ability_effect TEXT
);

-- --------------------------------------- STATS ---------------------------------------
CREATE TABLE Stats (
	stat_id INT PRIMARY KEY,
	hp INT,
    attack INT,
    defence INT,
    special_atk INT,
    special_def INT,
    speed INT
);

-- -~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~--~-~-~-~-~-~-~-~~-~-~ POKEMON REFERENCE -~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~
CREATE TABLE PokeRefs (
	poke_ref_id INT AUTO_INCREMENT PRIMARY KEY,
    pokemon_id INT,
    type_id INT,
    sub_type_id INT,
    region_id INT,
    ability_id INT,
    sub_ability_id INT,
    hidden_ability_id INT,
    stat_id INT,
    
FOREIGN KEY (pokemon_id) REFERENCES Pokemon(pokemon_id),
FOREIGN KEY (type_id) REFERENCES PokeTypes(type_id),
FOREIGN KEY (sub_type_id) REFERENCES SubPokeTypes(sub_type_id),
FOREIGN KEY (region_id) REFERENCES Regions(region_id),
FOREIGN KEY (ability_id) REFERENCES Abilities(ability_id),
FOREIGN KEY (sub_ability_id) REFERENCES SubAbilities(sub_ability_id),
FOREIGN KEY (hidden_ability_id) REFERENCES HiddenAbilities(hidden_ability_id),
FOREIGN KEY (stat_id) REFERENCES Stats(stat_id)
);


